export default class Food {
  constructor(ctx, x, y, width, height, image, id) {
    this.ctx = ctx;
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
    this.image = image;
    this.id = id; // Unique ID for the food
  }

  update(speed, frameTimeDelta, scaleRatio) {
    this.x -= speed * frameTimeDelta * scaleRatio; // Adjust position based on speed, frame time, and scale ratio
  }

  draw() {
    this.ctx.drawImage(this.image, this.x, this.y, this.width, this.height);
  }

  collideWith(sprite) {
    const adjustBy = 1.4;
    if (
      sprite.x < this.x + this.width / adjustBy &&
      sprite.x + sprite.width / adjustBy > this.x &&
      sprite.y < this.y + this.height / adjustBy &&
      sprite.height + sprite.y / adjustBy > this.y
    ) {
      return true;
    } else {
      return false;
    }
  }
}
